package calculadora;

/**
 *
 * @author pedro
 */
 public class Calculadora {

    private double resultado;

 public void somar (double a,double b){
     this.resultado= a + b;
  } 

 public void multiplicar (double a,double b){
     this.resultado= a * b;
  } 

public void dividir (double a,double b){
     this.resultado= a / b;
  } 

public void subtrair (double a,double b){
     this.resultado= a - b;
  } 

}



